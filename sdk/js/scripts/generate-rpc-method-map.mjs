import { readFileSync } from "node:fs";
import { mkdir, writeFile } from "node:fs/promises";

const rpcProtoURL = new URL("../../../api/rpc/peer.proto", import.meta.url);
const outputURL = new URL("../gizclaw/generated/rpc/method-map.ts", import.meta.url);
const generatedTypesURL = new URL("../gizclaw/generated/rpc/types.gen.ts", import.meta.url);
const schemaAliasURLs = [
  new URL("../../../api/type/server.json", import.meta.url),
];
const explicitTypeAliases = new Map([
  ["ClientGetInfoResponse", "RefreshInfo"],
  ["ClientGetIdentifiersResponse", "RefreshIdentifiers"],
]);

const methods = parseRPCMethods(readFileSync(rpcProtoURL, "utf8"));
const schemaCache = new Map();
const exportedTypeNames = readExportedTypeNames(generatedTypesURL);
const schemaAliases = buildSchemaAliases(schemaAliasURLs);

if (!Array.isArray(methods) || methods.length === 0) {
  throw new Error("api/rpc/peer.proto must define RpcMethod entries before generating RPC TypeScript.");
}

const typeNames = new Set();
for (const item of methods) {
  if (typeof item.method !== "string" || typeof item.request !== "string" || typeof item.response !== "string" || !Number.isSafeInteger(item.id)) {
    throw new Error("invalid RpcMethod entry");
  }
  typeNames.add(resolveGeneratedTypeName(item.request));
  typeNames.add(resolveGeneratedTypeName(item.response));
}

const imports = [...typeNames].sort().join(",\n  ");
const entries = methods
  .map((item) => `  ${JSON.stringify(item.method)}: {\n    request: ${resolveGeneratedTypeName(item.request)};\n    response: ${resolveGeneratedTypeName(item.response)};\n  };`)
  .join("\n");
const constants = methods.map((item) => `  ${JSON.stringify(item.method)}: ${JSON.stringify(item.method)},`).join("\n");
const ids = methods.map((item) => `  ${JSON.stringify(item.method)}: ${item.id},`).join("\n");

const text = `/* eslint-disable */\n// This file is generated by sdk/js/scripts/generate-rpc-method-map.mjs.\n// Do not edit by hand.\n\nimport type {\n  ${imports}\n} from \"./types.gen.ts\";\n\nexport type RPCMethodMap = {\n${entries}\n};\n\nexport type RPCMethodName = keyof RPCMethodMap;\n\nexport const RPC_METHODS = {\n${constants}\n} as const satisfies Record<RPCMethodName, RPCMethodName>;\n\nexport const RPC_METHOD_IDS = {\n${ids}\n} as const satisfies Record<RPCMethodName, number>;\n`;

await mkdir(new URL(".", outputURL), { recursive: true });
await writeFile(outputURL, text);

function parseRPCMethods(proto) {
  const lines = proto.split(/\r?\n/);
  const methods = [];
  let pending;
  for (const line of lines) {
    const comment = /^\s*\/\/\s*rpc:\s+(\S+)\s+request=(\w+)\s+response=(\w+)\s*$/.exec(line);
    if (comment != null) {
      pending = { method: comment[1], request: comment[2], response: comment[3] };
      continue;
    }

    const entry = /^\s*RPC_METHOD_[A-Z0-9_]+\s*=\s*(\d+)\s*;/.exec(line);
    if (entry == null) {
      continue;
    }
    if (pending == null) {
      if (!line.includes("RPC_METHOD_UNSPECIFIED")) {
        throw new Error(`RpcMethod entry missing rpc comment: ${line.trim()}`);
      }
      continue;
    }

    methods.push({ ...pending, id: Number(entry[1]) });
    pending = undefined;
  }
  return methods;
}

function resolveGeneratedTypeName(name) {
  const seen = new Set();
  let current = name;
  for (;;) {
    if (exportedTypeNames.has(current)) {
      return current;
    }
    if (seen.has(current)) {
      throw new Error(`cyclic RPC type alias for ${name}`);
    }
    seen.add(current);
    const next = explicitTypeAliases.get(current) ?? schemaAliases.get(current);
    if (next == null) {
      throw new Error(`RPC type ${name} resolves to ${current}, which is not exported by ${generatedTypesURL.pathname}`);
    }
    current = next;
  }
}

function buildSchemaAliases(urls) {
  const aliases = new Map();
  for (const url of urls) {
    const document = readJSON(url);
    const schemas = document.components?.schemas ?? {};
    for (const [name, schema] of Object.entries(schemas)) {
      if (schema && Object.keys(schema).length === 1 && typeof schema.$ref === "string") {
        aliases.set(name, resolveSchemaRefTypeName(schema.$ref, url));
      }
    }
  }
  return aliases;
}

function resolveSchemaRefTypeName(ref, baseURL) {
  const resolved = resolveSchemaRef(ref, baseURL);
  const schema = resolved.schema;
  if (schema && Object.keys(schema).length === 1 && typeof schema.$ref === "string") {
    return resolveSchemaRefTypeName(schema.$ref, resolved.documentURL);
  }
  return refTypeName(ref);
}

function resolveSchemaRef(ref, baseURL) {
  const marker = "#/components/schemas/";
  const index = ref.indexOf(marker);
  if (index < 0) {
    throw new Error(`unsupported schema ref: ${ref}`);
  }
  const documentURL = new URL(ref.slice(0, index) || baseURL.href, baseURL);
  const typeName = ref.slice(index + marker.length);
  const document = readJSON(documentURL);
  const schema = document.components?.schemas?.[typeName];
  if (schema == null) {
    throw new Error(`schema ref not found: ${ref}`);
  }
  return { documentURL, schema };
}

function refTypeName(ref) {
  const marker = "#/components/schemas/";
  const index = ref.indexOf(marker);
  if (index < 0) {
    throw new Error(`unsupported schema ref: ${ref}`);
  }
  return ref.slice(index + marker.length);
}

function readExportedTypeNames(url) {
  return new Set([...readFileSync(url, "utf8").matchAll(/^export type (\w+)\b/gm)].map((match) => match[1]));
}

function readJSON(url) {
  const key = url.href;
  const cached = schemaCache.get(key);
  if (cached != null) {
    return cached;
  }
  const parsed = JSON.parse(readFileSync(url, "utf8"));
  schemaCache.set(key, parsed);
  return parsed;
}
