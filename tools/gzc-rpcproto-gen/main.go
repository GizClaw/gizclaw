package main

import (
	"bytes"
	"encoding/json"
	"errors"
	"flag"
	"fmt"
	"os"
	"regexp"
	"sort"
	"strconv"
	"strings"
)

type schema struct {
	Ref                  string            `json:"$ref,omitempty"`
	Type                 any               `json:"type,omitempty"`
	Format               any               `json:"format,omitempty"`
	Enum                 []any             `json:"enum,omitempty"`
	Properties           map[string]schema `json:"properties,omitempty"`
	Required             []string          `json:"required,omitempty"`
	Items                *schema           `json:"items,omitempty"`
	AdditionalProperties any               `json:"additionalProperties,omitempty"`
	OneOf                []schema          `json:"oneOf,omitempty"`
	AnyOf                []schema          `json:"anyOf,omitempty"`
}

type openAPIDoc struct {
	Components struct {
		Schemas map[string]schema `json:"schemas"`
	} `json:"components"`
}

type generator struct {
	schemas        map[string]schema
	needed         map[string]bool
	enums          map[string][]string
	messageNumbers map[string]map[string]int32
	enumNumbers    map[string]map[string]int32
}

func main() {
	if err := run(os.Args[1:]); err != nil {
		fmt.Fprintf(os.Stderr, "gzc-rpcproto-gen: %v\n", err)
		os.Exit(1)
	}
}

func run(args []string) error {
	var schemaPath string
	var outPath string
	flags := flag.NewFlagSet("gzc-rpcproto-gen", flag.ContinueOnError)
	flags.StringVar(&schemaPath, "schema", "pkgs/gizclaw/api/rpcapi/rpc_resolved.json", "Resolved RPC OpenAPI schema")
	flags.StringVar(&outPath, "out", "api/rpc/payload.proto", "Generated RPC payload proto path")
	if err := flags.Parse(args); err != nil {
		return err
	}
	if schemaPath == "" || outPath == "" {
		return errors.New("-schema and -out are required")
	}
	data, err := os.ReadFile(schemaPath)
	if err != nil {
		return err
	}
	var doc openAPIDoc
	if err := json.Unmarshal(data, &doc); err != nil {
		return fmt.Errorf("%s: %w", schemaPath, err)
	}
	g := &generator{
		schemas:        doc.Components.Schemas,
		needed:         map[string]bool{},
		enums:          map[string][]string{},
		messageNumbers: map[string]map[string]int32{},
		enumNumbers:    map[string]map[string]int32{},
	}
	if err := g.loadExistingNumbers(outPath); err != nil {
		return err
	}
	if err := g.collectRPCPayloads(); err != nil {
		return err
	}
	out, err := g.emit()
	if err != nil {
		return err
	}
	return writeIfChanged(outPath, out)
}

func (g *generator) collectRPCPayloads() error {
	for _, root := range []struct {
		schemaName   string
		propertyName string
	}{
		{"RPCRequest", "params"},
		{"RPCResponse", "result"},
	} {
		s, ok := g.schemas[root.schemaName]
		if !ok {
			return fmt.Errorf("%s schema not found", root.schemaName)
		}
		prop, ok := s.Properties[root.propertyName]
		if !ok {
			return fmt.Errorf("%s.%s schema not found", root.schemaName, root.propertyName)
		}
		for _, item := range prop.OneOf {
			name, ok := schemaRefName(item.Ref)
			if !ok {
				return fmt.Errorf("%s.%s oneOf item missing schema ref", root.schemaName, root.propertyName)
			}
			g.mark(name)
		}
	}
	return nil
}

func (g *generator) mark(name string) {
	if name == "" || g.needed[name] {
		return
	}
	g.needed[name] = true
	s, ok := g.schemas[name]
	if !ok {
		return
	}
	g.markSchema(s)
}

func (g *generator) markSchema(s schema) {
	if name, ok := schemaRefName(s.Ref); ok {
		g.mark(name)
	}
	for _, child := range s.Properties {
		g.markSchema(child)
	}
	if s.Items != nil {
		g.markSchema(*s.Items)
	}
	for _, child := range s.OneOf {
		g.markSchema(child)
	}
	for _, child := range s.AnyOf {
		g.markSchema(child)
	}
	switch v := s.AdditionalProperties.(type) {
	case map[string]any:
		var child schema
		data, _ := json.Marshal(v)
		if json.Unmarshal(data, &child) == nil {
			g.markSchema(child)
		}
	}
}

func (g *generator) emit() ([]byte, error) {
	var buf bytes.Buffer
	buf.WriteString("// Code generated by tools/gzc-rpcproto-gen. DO NOT EDIT.\n\n")
	buf.WriteString("syntax = \"proto3\";\n\n")
	buf.WriteString("package gizclaw.rpc.v1;\n\n")
	buf.WriteString("import \"google/protobuf/struct.proto\";\n\n")
	buf.WriteString("option go_package = \"github.com/GizClaw/gizclaw-go/pkgs/gizclaw/api/rpcproto;rpcpb\";\n\n")

	names := sortedNeeded(g.needed)
	for _, name := range names {
		if err := g.collectEnums(name, g.schemas[name]); err != nil {
			return nil, err
		}
	}
	enumNames := sortedEnumNames(g.enums)
	for _, name := range enumNames {
		g.emitEnum(&buf, name, g.enums[name])
	}
	for _, name := range names {
		if err := g.emitMessage(&buf, name, g.schemas[name]); err != nil {
			return nil, err
		}
	}
	return buf.Bytes(), nil
}

func (g *generator) collectEnums(typeName string, s schema) error {
	if len(s.Enum) > 0 {
		values, err := stringEnumValues(s.Enum)
		if err != nil {
			return fmt.Errorf("%s enum: %w", typeName, err)
		}
		g.enums[typeName] = values
		return nil
	}
	for _, propName := range sortedSchemaKeys(s.Properties) {
		prop := s.Properties[propName]
		if len(prop.Enum) == 0 {
			continue
		}
		values, err := stringEnumValues(prop.Enum)
		if err != nil {
			return fmt.Errorf("%s.%s enum: %w", typeName, propName, err)
		}
		g.enums[typeName+upperCamel(propName)] = values
	}
	return nil
}

func (g *generator) emitEnum(buf *bytes.Buffer, name string, values []string) {
	buf.WriteString("enum " + name + " {\n")
	prefix := protoEnumPrefix(name)
	buf.WriteString("  " + prefix + "_UNSPECIFIED = 0;\n")
	seen := map[string]int{}
	usedNumbers := map[int32]bool{0: true}
	for _, value := range values {
		constName := prefix + "_" + protoEnumValue(value)
		if seen[constName] > 0 {
			constName += "_" + strconv.Itoa(seen[constName]+1)
		}
		seen[constName]++
		number := g.allocateEnumNumber(name, constName, usedNumbers)
		fmt.Fprintf(buf, "  %s = %d;\n", constName, number)
	}
	buf.WriteString("}\n\n")
}

func (g *generator) emitMessage(buf *bytes.Buffer, name string, s schema) error {
	if len(s.Enum) > 0 {
		return nil
	}
	if target, ok := schemaRefName(s.Ref); ok {
		buf.WriteString("message " + name + " {\n")
		fmt.Fprintf(buf, "  %s value = 1;\n", target)
		buf.WriteString("}\n\n")
		return nil
	}
	choices := s.OneOf
	if len(choices) == 0 {
		choices = s.AnyOf
	}
	if len(choices) > 0 {
		buf.WriteString("message " + name + " {\n")
		buf.WriteString("  oneof value {\n")
		usedNumbers := map[int32]bool{}
		for _, choice := range choices {
			typeName, fieldName, err := g.fieldType(name, "value", choice)
			if err != nil {
				return err
			}
			if refName, ok := schemaRefName(choice.Ref); ok {
				fieldName = lowerSnake(refName)
			}
			fieldName = uniqueFieldName(fieldName, nil)
			fmt.Fprintf(buf, "    %s %s = %d;\n", typeName, fieldName, g.allocateFieldNumber(name, fieldName, usedNumbers))
		}
		buf.WriteString("  }\n")
		buf.WriteString("}\n\n")
		return nil
	}
	buf.WriteString("message " + name + " {\n")
	if len(s.Properties) == 0 {
		if isOpenObject(s) {
			buf.WriteString("  google.protobuf.Struct fields = 1;\n")
		}
		buf.WriteString("}\n\n")
		return nil
	}
	required := requiredSet(s.Required)
	used := map[string]bool{}
	usedNumbers := map[int32]bool{}
	for _, propName := range sortedSchemaKeys(s.Properties) {
		prop := s.Properties[propName]
		typeName, fieldName, err := g.fieldType(name, propName, prop)
		if err != nil {
			return err
		}
		if !required[propName] && scalarOrEnum(typeName) && !strings.HasPrefix(typeName, "repeated ") && !strings.HasPrefix(typeName, "map<") {
			typeName = "optional " + typeName
		}
		fieldName = uniqueFieldName(fieldName, used)
		fmt.Fprintf(buf, "  %s %s = %d;\n", typeName, fieldName, g.allocateFieldNumber(name, fieldName, usedNumbers))
	}
	if isOpenObject(s) {
		fieldName := uniqueFieldName("extra_fields", used)
		fmt.Fprintf(buf, "  google.protobuf.Struct %s = %d;\n", fieldName, g.allocateFieldNumber(name, fieldName, usedNumbers))
	}
	buf.WriteString("}\n\n")
	return nil
}

func (g *generator) fieldType(parentName, propName string, s schema) (string, string, error) {
	if name, ok := schemaRefName(s.Ref); ok {
		return name, lowerSnake(propName), nil
	}
	if len(s.Enum) > 0 {
		return parentName + upperCamel(propName), lowerSnake(propName), nil
	}
	if s.Items != nil {
		itemType, _, err := g.fieldType(parentName, propName+"Item", *s.Items)
		if err != nil {
			return "", "", err
		}
		itemType = strings.TrimPrefix(itemType, "optional ")
		return "repeated " + itemType, lowerSnake(propName), nil
	}
	if mapValue, ok := mapValueSchema(s.AdditionalProperties); ok && len(s.Properties) == 0 {
		valueType, _, err := g.fieldType(parentName, propName+"Value", mapValue)
		if err != nil {
			return "", "", err
		}
		valueType = strings.TrimPrefix(valueType, "optional ")
		if strings.HasPrefix(valueType, "repeated ") || strings.HasPrefix(valueType, "map<") {
			valueType = "google.protobuf.Value"
		}
		return "map<string, " + valueType + ">", lowerSnake(propName), nil
	}
	switch schemaType(s.Type) {
	case "string":
		if schemaFormat(s.Format) == "byte" {
			return "bytes", lowerSnake(propName), nil
		}
		return "string", lowerSnake(propName), nil
	case "integer":
		switch schemaFormat(s.Format) {
		case "uint64":
			return "uint64", lowerSnake(propName), nil
		case "int32":
			return "int32", lowerSnake(propName), nil
		default:
			return "int64", lowerSnake(propName), nil
		}
	case "number":
		return "double", lowerSnake(propName), nil
	case "boolean":
		return "bool", lowerSnake(propName), nil
	case "object", "":
		return "google.protobuf.Struct", lowerSnake(propName), nil
	default:
		return "google.protobuf.Value", lowerSnake(propName), nil
	}
}

func stringEnumValues(values []any) ([]string, error) {
	out := make([]string, 0, len(values))
	for _, value := range values {
		s, ok := value.(string)
		if !ok {
			return nil, fmt.Errorf("non-string enum value %v", value)
		}
		out = append(out, s)
	}
	return out, nil
}

func mapValueSchema(value any) (schema, bool) {
	m, ok := value.(map[string]any)
	if !ok {
		return schema{}, false
	}
	var out schema
	data, _ := json.Marshal(m)
	if json.Unmarshal(data, &out) != nil {
		return schema{}, false
	}
	return out, true
}

func isOpenObject(s schema) bool {
	v, ok := s.AdditionalProperties.(bool)
	return ok && v
}

func schemaRefName(ref string) (string, bool) {
	const prefix = "#/components/schemas/"
	if !strings.HasPrefix(ref, prefix) {
		return "", false
	}
	return strings.TrimPrefix(ref, prefix), true
}

func schemaType(value any) string {
	switch v := value.(type) {
	case string:
		return v
	case []any:
		if len(v) > 0 {
			if s, ok := v[0].(string); ok {
				return s
			}
		}
	}
	return ""
}

func schemaFormat(value any) string {
	if s, ok := value.(string); ok {
		return s
	}
	return ""
}

func requiredSet(required []string) map[string]bool {
	out := map[string]bool{}
	for _, name := range required {
		out[name] = true
	}
	return out
}

func scalarOrEnum(typeName string) bool {
	switch typeName {
	case "bool", "bytes", "double", "fixed64", "float", "int32", "int64", "sfixed32", "sfixed64", "sint32", "sint64", "string", "uint32", "uint64":
		return true
	default:
		return regexp.MustCompile(`^[A-Z]\w*$`).MatchString(typeName)
	}
}

func sortedNeeded(m map[string]bool) []string {
	out := make([]string, 0, len(m))
	for name := range m {
		out = append(out, name)
	}
	sort.Strings(out)
	return out
}

func sortedSchemaKeys(m map[string]schema) []string {
	out := make([]string, 0, len(m))
	for name := range m {
		out = append(out, name)
	}
	sort.Strings(out)
	return out
}

func sortedEnumNames(m map[string][]string) []string {
	out := make([]string, 0, len(m))
	for name := range m {
		out = append(out, name)
	}
	sort.Strings(out)
	return out
}

func upperCamel(value string) string {
	parts := regexp.MustCompile(`[^A-Za-z0-9]+`).Split(value, -1)
	var out strings.Builder
	for _, part := range parts {
		if part == "" {
			continue
		}
		out.WriteString(strings.ToUpper(part[:1]))
		if len(part) > 1 {
			out.WriteString(part[1:])
		}
	}
	if out.Len() == 0 {
		return "Value"
	}
	return out.String()
}

func lowerSnake(value string) string {
	var out strings.Builder
	prevLower := false
	for _, r := range value {
		switch {
		case r >= 'A' && r <= 'Z':
			if prevLower {
				out.WriteByte('_')
			}
			out.WriteRune(r + ('a' - 'A'))
			prevLower = false
		case r >= 'a' && r <= 'z':
			out.WriteRune(r)
			prevLower = true
		case r >= '0' && r <= '9':
			out.WriteRune(r)
			prevLower = false
		default:
			if out.Len() > 0 && !strings.HasSuffix(out.String(), "_") {
				out.WriteByte('_')
			}
			prevLower = false
		}
	}
	name := strings.Trim(out.String(), "_")
	if name == "" {
		name = "field"
	}
	if name[0] >= '0' && name[0] <= '9' {
		name = "field_" + name
	}
	return name
}

func uniqueFieldName(name string, used map[string]bool) string {
	if used == nil {
		return name
	}
	if !used[name] {
		used[name] = true
		return name
	}
	base := name
	for i := 2; ; i++ {
		name = base + "_" + strconv.Itoa(i)
		if !used[name] {
			used[name] = true
			return name
		}
	}
}

func protoEnumPrefix(name string) string {
	return strings.ToUpper(lowerSnake(name))
}

func protoEnumValue(value string) string {
	name := strings.ToUpper(lowerSnake(value))
	if name == "" {
		return "VALUE"
	}
	return name
}

func isReservedFieldNumber(n int32) bool {
	return n >= 19000 && n <= 19999
}

func (g *generator) loadExistingNumbers(path string) error {
	data, err := os.ReadFile(path)
	if errors.Is(err, os.ErrNotExist) {
		return nil
	}
	if err != nil {
		return err
	}
	messageRe := regexp.MustCompile(`^\s*message\s+([A-Za-z_]\w*)\s*\{`)
	enumRe := regexp.MustCompile(`^\s*enum\s+([A-Za-z_]\w*)\s*\{`)
	fieldRe := regexp.MustCompile(`^\s*(?:optional\s+|repeated\s+)?(?:map<[^>]+>|[A-Za-z_]\w*(?:\.[A-Za-z_]\w*)*)\s+([A-Za-z_]\w*)\s*=\s*(\d+)\s*;`)
	enumValueRe := regexp.MustCompile(`^\s*([A-Z][A-Z0-9_]*)\s*=\s*(-?\d+)\s*;`)
	var messageName string
	var enumName string
	depth := 0
	for _, line := range strings.Split(string(data), "\n") {
		if messageName == "" && enumName == "" {
			if match := messageRe.FindStringSubmatch(line); match != nil {
				messageName = match[1]
				depth = braceDelta(line)
				if g.messageNumbers[messageName] == nil {
					g.messageNumbers[messageName] = map[string]int32{}
				}
				continue
			}
			if match := enumRe.FindStringSubmatch(line); match != nil {
				enumName = match[1]
				depth = braceDelta(line)
				if g.enumNumbers[enumName] == nil {
					g.enumNumbers[enumName] = map[string]int32{}
				}
				continue
			}
		}
		if messageName != "" {
			if match := fieldRe.FindStringSubmatch(line); match != nil {
				n, err := strconv.ParseInt(match[2], 10, 32)
				if err != nil {
					return fmt.Errorf("%s field %s.%s: %w", path, messageName, match[1], err)
				}
				g.messageNumbers[messageName][match[1]] = int32(n)
			}
			depth += braceDelta(line)
			if depth <= 0 {
				messageName = ""
			}
			continue
		}
		if enumName != "" {
			if match := enumValueRe.FindStringSubmatch(line); match != nil {
				n, err := strconv.ParseInt(match[2], 10, 32)
				if err != nil {
					return fmt.Errorf("%s enum %s.%s: %w", path, enumName, match[1], err)
				}
				g.enumNumbers[enumName][match[1]] = int32(n)
			}
			depth += braceDelta(line)
			if depth <= 0 {
				enumName = ""
			}
		}
	}
	return nil
}

func braceDelta(line string) int {
	return strings.Count(line, "{") - strings.Count(line, "}")
}

func (g *generator) allocateFieldNumber(messageName, fieldName string, used map[int32]bool) int32 {
	if n := g.messageNumbers[messageName][fieldName]; n > 0 && !used[n] {
		used[n] = true
		return n
	}
	n := nextAvailableNumber(g.messageNumbers[messageName], used, 536870911)
	if g.messageNumbers[messageName] == nil {
		g.messageNumbers[messageName] = map[string]int32{}
	}
	g.messageNumbers[messageName][fieldName] = n
	return n
}

func (g *generator) allocateEnumNumber(enumName, valueName string, used map[int32]bool) int32 {
	if n := g.enumNumbers[enumName][valueName]; n > 0 && !used[n] {
		used[n] = true
		return n
	}
	n := nextAvailableNumber(g.enumNumbers[enumName], used, 2147483647)
	if g.enumNumbers[enumName] == nil {
		g.enumNumbers[enumName] = map[string]int32{}
	}
	g.enumNumbers[enumName][valueName] = n
	return n
}

func nextAvailableNumber(existing map[string]int32, used map[int32]bool, max int32) int32 {
	var n int32 = 1
	for _, existingNumber := range existing {
		if existingNumber >= n {
			n = existingNumber + 1
		}
	}
	for isReservedFieldNumber(n) || used[n] || existingNumberUsed(existing, n) {
		n++
		if n > max {
			n = 1
		}
	}
	used[n] = true
	return n
}

func existingNumberUsed(existing map[string]int32, n int32) bool {
	for _, existingNumber := range existing {
		if existingNumber == n {
			return true
		}
	}
	return false
}

func writeIfChanged(path string, data []byte) error {
	if existing, err := os.ReadFile(path); err == nil && bytes.Equal(existing, data) {
		return nil
	}
	return os.WriteFile(path, data, 0644)
}
